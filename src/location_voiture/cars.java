    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String query;
        String reg =carReg.getText();
        
        
        if (reg.isEmpty())
        {
            JOptionPane.showMessageDialog(new JFrame(), "Missing car registration number", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else
        {
            if(!isNumber(reg))
            {
            JOptionPane.showMessageDialog(new JFrame(), "id must be  number", "ERROR", JOptionPane.ERROR_MESSAGE);
            return ;
            }
            
            int CarReg =Integer.parseInt(carReg.getText());
              try{
                    
                    Connection con =DriverManager.getConnection(SUrl,SUser,SPass);
                    query = "DELETE FROM cartb WHERE carReg = ?";
                    
            try (PreparedStatement statement = con.prepareStatement(query)) {
                
                statement.setInt(1, CarReg);
                
                int rowsAffected = statement.executeUpdate();
                
                
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(new JFrame(), "Row deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(new JFrame(), "No rows deleted. Car registration not found", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            
            con.close();
                    
              }catch(Exception e){
                   System.out.print(e.getMessage());
                   }
              
              displaycars();
             
        }

        
    }//GEN-LAST:event_jButton2ActionPerformed

